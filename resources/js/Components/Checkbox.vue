<script setup>
import { computed } from 'vue';

const emit = defineEmits(['update:checked']);

const props = defineProps({
    checked: {
        type: [Array, Boolean],
        required: true,
    },
    value: {
        default: null,
    },
});

const proxyChecked = computed({
    get() {
        return props.checked;
    },

    set(val) {
        emit('update:checked', val);
    },
});
</script>

<template>
    <input
        type="checkbox"
        :value="value"
        v-model="proxyChecked"
        class="h-5 w-5 rounded border-slate-300 text-emerald-500 transition-all duration-200 cursor-pointer shadow-sm focus:ring-2 focus:ring-emerald-400 focus:border-emerald-500"
    />
</template>
